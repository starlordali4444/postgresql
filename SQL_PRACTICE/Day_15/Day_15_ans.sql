WITH
	ORDER_RANK AS (
		SELECT
			*,
			DENSE_RANK() OVER (
				PARTITION BY
					CUST_ID
				ORDER BY
					ORDER_DATE ASC
			) AS ORD_RNK
		FROM
			SALES.ORDERS
	)
SELECT
	C.FIRST_NAME,
	O.ORDER_ID,
	O.ORDER_DATE,
	O.NET_TOTAL
FROM
	ORDER_RANK O
	JOIN CUSTOMERS.CUSTOMERS C ON C.CUSTOMER_ID = O.CUST_ID
WHERE
	ORD_RNK = 1

