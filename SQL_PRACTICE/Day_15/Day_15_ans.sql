	RANKED AS (
		SELECT
			*,
			RANK() OVER (
				ORDER BY
					NET_TOTAL DESC
			) AS RNK,
			DENSE_RANK() OVER (
				ORDER BY
					NET_TOTAL DESC
			) AS D_RNK
		FROM
			SALES.ORDERS
	)
