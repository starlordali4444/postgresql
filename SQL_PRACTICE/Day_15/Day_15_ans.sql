	ORDER_ID,
	STORE_ID,
	ORDER_STATUS,
	COUNT(ORDER_ID) OVER (
		PARTITION BY
			STORE_ID,
			ORDER_STATUS
	) AS COMBINATION_COUNT
FROM
	SALES.ORDERS

