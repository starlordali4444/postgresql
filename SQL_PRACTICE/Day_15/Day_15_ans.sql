WITH
	DUP_CHECK AS (
		SELECT
			*,
			ROW_NUMBER() OVER (
				PARTITION BY
					FIRST_NAME,
					DEPT_ID
				ORDER BY
					EMPLOYEE_ID
			) AS DUP_RNK
		FROM
			STORES.EMPLOYEES
	)
SELECT
	*
FROM
	DUP_CHECK
WHERE
	DUP_RNK = 1;
