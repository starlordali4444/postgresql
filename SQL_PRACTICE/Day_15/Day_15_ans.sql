SELECT
	*,
	SUM(NET_TOTAL) OVER (
		ORDER BY
			ORDER_DATE ROWS BETWEEN UNBOUNDED PRECEDING
			AND CURRENT ROW
	) AS RUNNING_TOTAL
FROM
	SALES.ORDERS
ORDER BY
	ORDER_DATE;

