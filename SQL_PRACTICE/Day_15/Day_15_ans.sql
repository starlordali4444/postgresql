WITH
	STORE_REV AS (
		SELECT
			P.PRODUCT_ID,
			B.CATEGORY_ID,
			SUM(OI.NET_AMOUNT) AS TOTAL_REVENUE
		FROM
			PRODUCTS.PRODUCTS P
			JOIN SALES.ORDER_ITEMS OI ON P.PRODUCT_ID = OI.PROD_ID
			JOIN CORE.DIM_BRAND B USING (BRAND_ID)
		GROUP BY
			P.PRODUCT_ID,
			B.CATEGORY_ID
	),
	RANKED AS (
		SELECT
			*,
			DENSE_RANK() OVER (
				PARTITION BY
					CATEGORY_ID
				ORDER BY
					TOTAL_REVENUE DESC
			) AS RNK
		FROM
			STORE_REV
	)
SELECT
	*
FROM
	RANKED
WHERE
	RNK <= 3;

