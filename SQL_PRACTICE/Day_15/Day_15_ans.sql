		SELECT
			*,
			ROW_NUMBER() OVER (
				PARTITION BY
					EMAIL
			) AS EMAIL_COUNT
		FROM
			CUSTOMERS.CUSTOMERS
	)
SELECT
	*
FROM
	DUPLICATE_CHECK
WHERE
	EMAIL_COUNT > 1

