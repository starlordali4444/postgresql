-- ANSWER 13
WITH
	ORDER_RANK AS (
		SELECT
			*,
			DENSE_RANK() OVER (
				PARTITION BY
					CUST_ID
				ORDER BY
					ORDER_DATE ASC
			) AS ORD_RNK
		FROM
			SALES.ORDERS
	)
SELECT
	C.FIRST_NAME,
	O.ORDER_ID,
	O.ORDER_DATE,
	O.NET_TOTAL
FROM
	ORDER_RANK O
	JOIN CUSTOMERS.CUSTOMERS C ON C.CUSTOMER_ID = O.CUST_ID
WHERE
	ORD_RNK = 1

-- ANSWER 14
WITH
	STORE_REV AS (
		SELECT
			STORE_ID,
			SUM(NET_TOTAL) AS TOTAL_REV
		FROM
			SALES.ORDERS
		GROUP BY
			STORE_ID
	),
	STORE_REV_RANK AS (
		SELECT
			*,
			ROW_NUMBER() OVER (
				PARTITION BY
					REGION_ID
				ORDER BY
					TOTAL_REV
			) AS RNK
		FROM
			STORE_REV
			JOIN STORES.STORES USING (STORE_ID)
	)
SELECT
	*
FROM
	STORE_REV_RANK
WHERE
	RNK <= 2;

