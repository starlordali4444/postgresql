WITH
	CLEAN AS (
		SELECT
			DATE_TRUNC('day', ORDER_DATE) AS DAYS,
			ROUND(AVG(NET_TOTAL), 2) AS AVERAGE
		FROM
			SALES.ORDERS
		GROUP BY
			DATE_TRUNC('day', ORDER_DATE)
	),
	AVG_CALC AS (
		SELECT
			*,
			ROUND(
				AVG(AVERAGE) OVER (
					ORDER BY
						DAYS ROWS BETWEEN 6 PRECEDING
						AND CURRENT ROW
				),
				2
			) AS AVG_7_DAYS,
			ROUND(
				AVG(AVERAGE) OVER (
					ORDER BY
						DAYS ROWS BETWEEN 29 PRECEDING
						AND CURRENT ROW
				),
				2
			) AS AVG_30_DAYS
		FROM
			CLEAN
	)
SELECT
	*,
	CASE
		WHEN AVG_7_DAYS > AVG_30_DAYS THEN 'BUY SIGNAL'
		ELSE 'SELL SIGNAL'
	END AS SUGGESTION
FROM
	AVG_CALC

