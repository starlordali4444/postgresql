WITH
	RUN_TOTAL AS (
		SELECT
			*,
			SUM(NET_TOTAL) OVER (
				PARTITION BY
					STORE_ID
				ORDER BY
					ORDER_DATE,
					ORDER_ID
			) AS RUNNING_TOTAL
		FROM
			SALES.ORDERS
	)
SELECT
	STORE_ID,
	MIN(ORDER_DATE) AS FIRST_ORDER_CROSS_1CR
FROM
	RUN_TOTAL
WHERE
	RUNNING_TOTAL >= 10000000
GROUP BY
	STORE_ID
ORDER BY
	FIRST_ORDER_CROSS_1CR
LIMIT
	1;

