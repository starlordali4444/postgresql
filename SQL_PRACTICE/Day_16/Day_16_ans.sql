-- ANSWER 16
WITH
	DAILY_REV AS (
		SELECT
			DATE_TRUNC('day', ORDER_DATE) AS DAYS,
			ROUND(AVG(NET_TOTAL), 2) AS AVG_REV
		FROM
			SALES.ORDERS
		GROUP BY
			DATE_TRUNC('day', ORDER_DATE)
	),
	AVG_CALC AS (
		SELECT
			*,
			ROUND(
				AVG(AVG_REV) OVER (
					ORDER BY
						DAYS ROWS BETWEEN 6 PRECEDING
						AND CURRENT ROW
				),
				0
			) AS WEEKLY_AVG
		FROM
			DAILY_REV
	)
SELECT
	*,
	ROUND(AVG_REV * 100.0 / WEEKLY_AVG, 2) AS PCT,
	CASE
		WHEN AVG_REV * 100.0 / WEEKLY_AVG > 200 THEN 'MEGA SPIKE'
		ELSE 'Normal'
	END AS SPIKE_TYPE
FROM
	AVG_CALC

-- ANSWER 17
WITH
	CLEAN AS (
		SELECT
			STORE_ID,
			DATE_TRUNC('day', ORDER_DATE) AS DATE,
			SUM(NET_TOTAL) AS DAILY_TOTAL
		FROM
			SALES.ORDERS
		GROUP BY
			STORE_ID,
			DATE_TRUNC('day', ORDER_DATE)
	),
	-- A smooth trend = values change gradually (low fluctuation) STDDEV()
	-- A noisy trend = values jump up/down a lot
	-- STDDEV - avg value of how values are far from mean
	FINAL_TABLE AS (
		SELECT
			*,
			ROUND(
				AVG(DAILY_TOTAL) OVER (
					PARTITION BY
						STORE_ID
					ORDER BY
						DATE ROWS BETWEEN 6 PRECEDING
						AND CURRENT ROW
				),
				2
			) AS AVG_7_DAY
		FROM
			CLEAN
	)
SELECT
	STORE_ID,
	round(STDDEV(AVG_7_DAY),2) AS DEVIATION
FROM
	FINAL_TABLE
GROUP BY
	STORE_ID
ORDER BY
	DEVIATION
LIMIT
	1

