SELECT
	S.STORE_ID,
	S.STORE_NAME,
	SUM(OI.NET_AMOUNT) AS STORE_REVENUE,
	(
		SELECT
			SUM(OI.NET_AMOUNT)
		FROM
			SALES.ORDER_ITEMS OI
	) AS TOTAL_REVENUE,
	ROUND(
		SUM(OI.NET_AMOUNT) * 100.0 /
		(
		SELECT
			SUM(OI.NET_AMOUNT)
		FROM
			SALES.ORDER_ITEMS OI
		),2
	) AS PCT_OF_TOTAL
FROM
	STORES.STORES S
	JOIN SALES.ORDERS O USING (STORE_ID)
	JOIN SALES.ORDER_ITEMS OI USING (ORDER_ID)
GROUP BY
	S.STORE_ID,
	S.STORE_NAME
ORDER BY PCT_OF_TOTAL;

