SELECT
	CUST_ID,
	TOTAL_SPEND
FROM
	(
		SELECT
			CUST_ID,
			SUM(NET_TOTAL) AS TOTAL_SPEND
		FROM
			SALES.ORDERS
		GROUP BY
			CUST_ID
	) AS CUSTOMER_TOTAL
WHERE
	TOTAL_SPEND > (
		SELECT
			TOTAL_SPEND
		FROM
			(
				SELECT
					CUST_ID,
					SUM(NET_TOTAL) AS TOTAL_SPEND
				FROM
					SALES.ORDERS
				GROUP BY
					CUST_ID
				ORDER BY
					TOTAL_SPEND
				LIMIT
					1
				OFFSET
					(
						SELECT
							COUNT(DISTINCT CUST_ID) / 2
						FROM
							SALES.ORDERS
					)
			) AS MEDIAN_TABLE
	)

