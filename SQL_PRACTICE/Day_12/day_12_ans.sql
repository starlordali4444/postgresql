SELECT
	*
FROM
	CORE.DIM_CATEGORY
	JOIN ( --product count and avg product price
		SELECT
			B.CATEGORY_ID,
			COUNT(DISTINCT P.PRODUCT_ID) AS PRODUCT_COUNT,
			ROUND(AVG(P.PRICE), 2) AS PRODUCT_AVG
		FROM
			PRODUCTS.PRODUCTS P
			JOIN CORE.DIM_BRAND B USING (BRAND_ID)
		GROUP BY
			B.CATEGORY_ID
	) USING (CATEGORY_ID)
	JOIN ( --total revenue category wise
		SELECT
			B.CATEGORY_ID,
			SUM(NET_AMOUNT) AS REVENUE
		FROM
			SALES.ORDER_ITEMS OI
			JOIN PRODUCTS.PRODUCTS P ON OI.PROD_ID = P.PRODUCT_ID
			JOIN CORE.DIM_BRAND B USING (BRAND_ID)
		GROUP BY
			B.CATEGORY_ID
	) USING (CATEGORY_ID)
	JOIN ( --derived table for top product by revenue
		SELECT
			T1.CATEGORY_ID,
			T1.PRODUCT_ID AS BEST_PRODUCT
		FROM
			( --all product with its revenue category wise
				SELECT
					P.PRODUCT_ID,
					B.CATEGORY_ID,
					SUM(NET_AMOUNT) AS PRODUCT_REVENUE
				FROM
					SALES.ORDER_ITEMS OI
					JOIN PRODUCTS.PRODUCTS P ON P.PRODUCT_ID = OI.PROD_ID
					JOIN CORE.DIM_BRAND B USING (BRAND_ID)
				GROUP BY
					P.PRODUCT_ID,
					B.CATEGORY_ID
				ORDER BY
					B.CATEGORY_ID ASC,
					PRODUCT_REVENUE DESC
			) T1
			JOIN (
				-- B) Max product price, category wise (by grouping category in same sub-que	ry)
				SELECT
					CATEGORY_ID,
					MAX(PRODUCT_REVENUE) AS MAX_REVENUE
				FROM
					(
						SELECT
							P.PRODUCT_ID,
							B.CATEGORY_ID,
							SUM(NET_AMOUNT) AS PRODUCT_REVENUE
						FROM
							SALES.ORDER_ITEMS OI
							JOIN PRODUCTS.PRODUCTS P ON P.PRODUCT_ID = OI.PROD_ID
							JOIN CORE.DIM_BRAND B USING (BRAND_ID)
						GROUP BY
							P.PRODUCT_ID,
							B.CATEGORY_ID
						ORDER BY
							B.CATEGORY_ID ASC,
							PRODUCT_REVENUE DESC
					)
				GROUP BY
					CATEGORY_ID
			) T2 ON T1.CATEGORY_ID = T2.CATEGORY_ID
			AND T1.PRODUCT_REVENUE = T2.MAX_REVENUE --IMP: match product price & category in both tables
	) USING (CATEGORY_ID)

