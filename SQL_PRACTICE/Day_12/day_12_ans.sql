SELECT
	STORE_ID,
	DELIVERED_ORDER_REVENUE
FROM
	(
		SELECT
			STORE_ID,
			SUM(NET_TOTAL) AS DELIVERED_ORDER_REVENUE
		FROM
			SALES.ORDERS
		WHERE
			ORDER_STATUS = 'Delivered'
		GROUP BY
			STORE_ID
	) STORE_REVENUE
WHERE
	DELIVERED_ORDER_REVENUE > (
		SELECT
			AVG(DELIVERED_ORDER_REVENUE)
		FROM
			(
				SELECT
					SUM(NET_TOTAL) AS DELIVERED_ORDER_REVENUE
				FROM
					SALES.ORDERS
				WHERE
					ORDER_STATUS = 'Delivered'
				GROUP BY
					STORE_ID
			) AVG_STORE_REVENUE
	)
ORDER BY
	DELIVERED_ORDER_REVENUE;

