SELECT
	S.STORE_ID,
	S.STORE_NAME,
	STORE_REV.STORE_REVENUE,
	COMPANY_REV.COMPANY_REVENUE,
	ROUND(
		STORE_REV.STORE_REVENUE * 100.0 / COMPANY_REV.COMPANY_REVENUE,
		2
	) PCT_STORE_PER_COMPANY
FROM
	(
		SELECT
			STORE_ID,
			SUM(NET_TOTAL) AS STORE_REVENUE
		FROM
			SALES.ORDERS
		GROUP BY
			STORE_ID
	) STORE_REV
	JOIN STORES.STORES S USING (STORE_ID)
	CROSS JOIN (
		SELECT
			SUM(NET_TOTAL) AS COMPANY_REVENUE
		FROM
			SALES.ORDERS
	) COMPANY_REV
ORDER BY
	STORE_REV.STORE_REVENUE DESC

