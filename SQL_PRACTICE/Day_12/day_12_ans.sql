SELECT
	S.STORE_ID,
	S.STORE_NAME,
	R.REGION_NAME,
	EMP.HEADCOUNT,
	EMP.AVG_SALARY,
	ORD.REVENUE,
	ORD.ORDER_COUNT,
	RET.TOTAL_RETURNED,
	RET.TOTAL_RETURNED::NUMERIC / ORD.ORDER_COUNT AS RETURN_RATE,
	SUPP.AVG_TICKET_RESOLUTION_HRS,
	CASE
		WHEN ORD.REVENUE > 100000
		AND (
			RET.TOTAL_RETURNED::NUMERIC / NULLIF(ORD.ORDER_COUNT, 0)
		) < 0.05 THEN 'Excellent'
		WHEN ORD.REVENUE > 50000
		OR (
			RET.TOTAL_RETURNED::NUMERIC / NULLIF(ORD.ORDER_COUNT, 0)
		) < 0.10 THEN 'Healthy'
		WHEN ORD.REVENUE IS NULL THEN 'No Data'
		ELSE 'Needs Attention'
	END AS STORE_HEALTH_SCORE
FROM
	STORES.STORES S
	LEFT JOIN CORE.DIM_REGION R USING (REGION_ID)
	LEFT JOIN (
		SELECT
			STORE_ID,
			COUNT(*) AS HEADCOUNT,
			ROUND(AVG(SALARY), 0) AS AVG_SALARY
		FROM
			STORES.EMPLOYEES
		GROUP BY
			STORE_ID
	) EMP USING (STORE_ID)
	LEFT JOIN (
		SELECT
			STORE_ID,
			COUNT(*) AS ORDER_COUNT,
			SUM(NET_TOTAL) AS REVENUE
		FROM
			SALES.ORDERS
		GROUP BY
			STORE_ID
	) ORD USING (STORE_ID)
	LEFT JOIN (
		SELECT
			STORE_ID,
			COUNT(*) AS TOTAL_RETURNED
		FROM
			SALES.RETURNS
			JOIN SALES.ORDERS USING (ORDER_ID)
		GROUP BY
			STORE_ID
	) RET USING (STORE_ID)
	LEFT JOIN (
		SELECT
			E.STORE_ID,
			ROUND(
				AVG(
					ROUND(
						EXTRACT(
							'EPOCH'
							FROM
								RESOLVED_DATE - CREATED_DATE
						) / 3600,
						1
					)
				),
				2
			) AS AVG_TICKET_RESOLUTION_HRS
		FROM
			SUPPORT.TICKETS T
			LEFT JOIN STORES.EMPLOYEES E ON E.EMPLOYEE_ID = T.AGENT_ID
		GROUP BY
			STORE_ID
	) SUPP USING (STORE_ID)