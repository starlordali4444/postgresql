	FIRST_NAME,
	SALARY,
	DEPT.DEPT_AVG,
	COMP.COMP_AVG,
	CASE
		WHEN SALARY > DEPT.DEPT_AVG
		AND SALARY > COMP.COMP_AVG THEN 'Above Both'
		WHEN SALARY > DEPT.DEPT_AVG
		AND SALARY < COMP.COMP_AVG THEN 'Above Department Average only'
		WHEN SALARY < DEPT.DEPT_AVG
		AND SALARY > COMP.COMP_AVG THEN 'Above Company Average only'
		ELSE 'Below Both'
	END AS CLASSIFICATION
FROM
	STORES.EMPLOYEES
	JOIN (
		SELECT
			DEPT_ID,
			ROUND(AVG(SALARY), 2) AS DEPT_AVG
		FROM
			STORES.EMPLOYEES
		GROUP BY
			DEPT_ID
	) DEPT USING (DEPT_ID)
	CROSS JOIN (
		SELECT
			ROUND(AVG(SALARY), 2) AS COMP_AVG
		FROM
			STORES.EMPLOYEES
	) COMP

