SELECT
	EMP.STORE_ID,
	EMP.EMP_AVG_SALARY,
	COMP.COMP_AVG_SALARY,
	ABS(EMP.EMP_AVG_SALARY - COMP.COMP_AVG_SALARY) AS AVG_SALARY_DIFFERENCE,
	STORE.TOTAL_REVENUE,
	STORE2.AVG_REVENUE,
	ABS(STORE.TOTAL_REVENUE - STORE2.AVG_REVENUE) AS AVG_REVENUE_DIFFERENCE
FROM
	(
		SELECT
			STORE_ID,
			ROUND(AVG(SALARY), 2) AS EMP_AVG_SALARY
		FROM
			STORES.EMPLOYEES
		GROUP BY
			STORE_ID
	) EMP
	CROSS JOIN (
		SELECT
			ROUND(AVG(SALARY), 2) AS COMP_AVG_SALARY
		FROM
			STORES.EMPLOYEES
	) COMP
	JOIN (
		SELECT
			STORE_ID,
			ROUND(SUM(NET_TOTAL), 2) AS TOTAL_REVENUE
		FROM
			SALES.ORDERS
		GROUP BY
			STORE_ID
	) STORE USING (STORE_ID)
	CROSS JOIN (
		SELECT
			ROUND(AVG(T.TOTAL_REVENUE), 2) AS AVG_REVENUE
		FROM
			(
				SELECT
					STORE_ID,
					ROUND(SUM(NET_TOTAL), 2) AS TOTAL_REVENUE
				FROM
					SALES.ORDERS
				GROUP BY
					STORE_ID
			) T
	) STORE2
ORDER BY
	STORE.TOTAL_REVENUE,
	STORE2.AVG_REVENUE DESC;

