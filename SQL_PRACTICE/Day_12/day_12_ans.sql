SELECT
	CURR.MONTH,
	CURR.TOTAL_REVENUE
FROM
	(
		SELECT
			DATE_TRUNC('months', ORDER_DATE) AS MONTH,
			EXTRACT(
				YEAR
				FROM
					ORDER_DATE
			) AS YEAR_NO,
			SUM(NET_TOTAL) AS TOTAL_REVENUE
		FROM
			SALES.ORDERS
		GROUP BY
			DATE_TRUNC('months', ORDER_DATE),
			EXTRACT(
				YEAR
				FROM
					ORDER_DATE
			)
	) CURR
WHERE
	CURR.TOTAL_REVENUE > ALL (
		SELECT
			PREV.TOTAL_REVENUE
		FROM
			(
				SELECT
					DATE_TRUNC('months', ORDER_DATE) AS MONTH,
					EXTRACT(
						YEAR
						FROM
							ORDER_DATE
					) AS YEAR_NO,
					SUM(NET_TOTAL) AS TOTAL_REVENUE
				FROM
					SALES.ORDERS
				GROUP BY
					DATE_TRUNC('months', ORDER_DATE),
					EXTRACT(
						YEAR
						FROM
							ORDER_DATE
					)
			) PREV
		WHERE
			PREV.YEAR_NO = CURR.YEAR_NO - 1
	)
ORDER BY
	CURR.MONTH;

