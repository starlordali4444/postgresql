SELECT
	C.CATEGORY_NAME,
	CS.PRODUCT_COUNT,
	CS.TOTAL_REVENUE,
	CS.AVG_PRODUCT_PRICE
FROM
	(
		SELECT
			B.CATEGORY_ID,
			COUNT(DISTINCT P.PRODUCT_ID) AS PRODUCT_COUNT,
			SUM(OI.NET_AMOUNT) AS TOTAL_REVENUE,
			ROUND(AVG(P.PRICE), 0) AS AVG_PRODUCT_PRICE
		FROM
			PRODUCTS.PRODUCTS P
			JOIN CORE.DIM_BRAND B USING (BRAND_ID)
			JOIN SALES.ORDER_ITEMS OI ON P.PRODUCT_ID = OI.PROD_ID
		GROUP BY
			B.CATEGORY_ID
	) CS
	JOIN CORE.DIM_CATEGORY C USING (CATEGORY_ID)
ORDER BY
	CS.TOTAL_REVENUE DESC

