SELECT
	O.ORDER_ID,
	(
		SELECT
			ROUND(AVG(O2.NET_TOTAL), 2)
		FROM
			SALES.ORDERS O2
		WHERE
			O2.STORE_ID = O.STORE_ID
	)
FROM
	SALES.ORDERS O
WHERE
	O.NET_TOTAL > (
		SELECT
			ROUND(AVG(O2.NET_TOTAL), 2) AS AVG_NET_TOTAL
		FROM
			SALES.ORDERS O2
		WHERE
			O2.STORE_ID = O.STORE_ID
	)

