WITH
	FILTERED_DEPT AS (
		SELECT
			DEPT_ID
		FROM
			CORE.DIM_DEPARTMENT
		WHERE
			DEPT_NAME = 'Sales'
	),
	FILTERED_EMP AS (
		SELECT
			SALARY
		FROM
			STORES.EMPLOYEES
		WHERE
			DEPT_ID IN (
				SELECT
					DEPT_ID
				FROM
					FILTERED_DEPT
			)
	),
	AVG_CALC AS (
		SELECT
			ROUND(AVG(SALARY),0) AS AVG_SALARY
		FROM
			FILTERED_EMP
	)
SELECT
	E.EMPLOYEE_ID,
	E.FIRST_NAME,
	E.SALARY,
	A.AVG_SALARY
FROM
	STORES.EMPLOYEES E
	CROSS JOIN AVG_CALC A
	WHERE E.SALARY > A.AVG_SALARY