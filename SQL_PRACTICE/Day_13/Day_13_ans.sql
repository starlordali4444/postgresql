WITH
	CUSTOMER_SPENDING AS (
		SELECT
			CUST_ID,
			SUM(NET_TOTAL) AS CUSTOMER_SPEND
		FROM
			SALES.ORDERS
		GROUP BY
			CUST_ID
	),
	CUSTOMER_REVIEWS AS (
		SELECT
			CUSTOMER_ID,
			COUNT(REVIEW_ID) AS CUSTOMER_REVIEW
		FROM
			CUSTOMERS.REVIEWS
		GROUP BY
			CUSTOMER_ID
	),
	CUSTOMER_RETURNS AS (
		SELECT
			CUST_ID,
			COUNT(RETURN_ID) AS CUSTOMER_RETURNED
		FROM
			SALES.RETURNS
			JOIN SALES.ORDERS USING (ORDER_ID)
		GROUP BY
			CUST_ID
	)
SELECT
	C.CUSTOMER_ID,
	COALESCE(CS.CUSTOMER_SPEND,0) AS SPENDING,
	COALESCE(CR.CUSTOMER_REVIEW,0)AS REVIEW_ACTIVITY,
	COALESCE(RE.CUSTOMER_RETURNED,0)AS RETURN_RATE
FROM
	CUSTOMERS.CUSTOMERS C
	LEFT JOIN CUSTOMER_SPENDING CS ON C.CUSTOMER_ID = CS.CUST_ID
	LEFT JOIN CUSTOMER_REVIEWS CR ON C.CUSTOMER_ID = CR.CUSTOMER_ID
	LEFT JOIN CUSTOMER_RETURNS RE ON C.CUSTOMER_ID = RE.CUST_ID

