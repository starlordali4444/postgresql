	P.PRODUCT_NAME,
	P.PRICE
FROM
	PRODUCTS.PRODUCTS P
WHERE
	NOT EXISTS (
		SELECT
			1
		FROM
			SALES.RETURNS R
		WHERE
			R.PROD_ID = P.PRODUCT_ID
	)
ORDER BY
	P.PRODUCT_NAME;

