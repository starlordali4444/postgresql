SELECT
	O.CUST_ID,
	C.FIRST_NAME,
	C.EMAIL,
	(
		SELECT
			COUNT(*)
		FROM
			SALES.ORDERS O2
		WHERE
			O2.CUST_ID = O.CUST_ID
	) AS ORDER_COUNT
FROM
	SALES.ORDERS O
	JOIN CUSTOMERS.CUSTOMERS C ON O.CUST_ID = C.CUSTOMER_ID
ORDER BY
	ORDER_COUNT DESC

